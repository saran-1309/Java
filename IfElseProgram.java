package AllProgram;

public class IfElseProgram {

	public static void main(String[] args) {
	/* if else is conditional statement that is used in specific block of a program if the condition is true or false
	 * NOTE-->>> True will execute the if part and false will execute the else part.
	 */
		int age=7;
		if(age>=18) {
			System.out.println("Voting is allowed");
		}
		if(age<18) {
			System.out.println("Voting is not allowed");
		}
		if(age>80) {
			System.out.println("Voting is allowed in house itself");
		}
	}
	}